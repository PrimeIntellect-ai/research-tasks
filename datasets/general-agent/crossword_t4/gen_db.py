def generate_word_bank() -> list[dict]:
    """Generate a word bank with words of various lengths and categories."""
    words = []
    used_words = set()

    for category, word_list in CATEGORIES.items():
        for word in word_list:
            w = word.upper()
            if w in used_words:
                continue
            if len(w) < 3 or len(w) > 9:
                continue
            used_words.add(w)
            # Generate a plausible clue
            clue = f"Related to {category}"
            words.append(
                {
                    "word": w,
                    "clue": clue,
                    "category": category,
                }
            )

    # Add some 5-letter filler words across categories
    filler = [
        ("PIXEL", "Smallest unit of a digital image", "tech"),
        ("BYTE", "Eight bits of data", "tech"),
        ("ARRAY", "Ordered collection of elements", "tech"),
        ("STACK", "LIFO data structure", "tech"),
        ("CLOUD", "Remote computing infrastructure", "tech"),
        ("STEAM", "Water vapor", "science"),
        ("CORE", "Central part", "science"),
        ("SHELL", "Hard outer covering", "nature"),
        ("SPIKE", "Sharp projection", "nature"),
        ("TRAIL", "Path through wilderness", "nature"),
        ("BLUFF", "Steep cliff", "nature"),
        ("CACTUS", "Desert plant", "nature"),
        ("FABLE", "Moral story", "words"),
        ("GUILE", "Cunning deceit", "words"),
        ("MIRTH", "Amusement", "words"),
        ("PENNY", "Small coin", "commerce"),
        ("QUOTA", "Allocated share", "commerce"),
        ("TALLY", "Running count", "commerce"),
        ("VALVE", "Flow regulator", "objects"),
        ("WEDGE", "Tapered shape", "objects"),
        ("GEAR", "Toothed wheel", "objects"),
        ("PILOT", "Aircraft navigator", "objects"),
        ("SIREN", "Warning sound", "objects"),
        ("STAMP", "Postage mark", "objects"),
        ("PYLON", "Tall vertical support structure", "structures"),
        ("SPIRE", "Tall pointed structure on a building", "structures"),
        ("PANEL", "Flat section of a structure", "structures"),
        ("TRADE", "Exchange of goods", "commerce"),
    ]
    for word, clue, cat in filler:
        if word not in used_words:
            used_words.add(word)
            words.append({"word": word, "clue": clue, "category": cat})

    return words


def main():
    out_dir = Path(__file__).parent
    word_bank = generate_word_bank()
    grid_size = 11

    # 11x11 empty grid
    grid = [["" for _ in range(grid_size)] for _ in range(grid_size)]

    db = {
        "grid_size": grid_size,
        "grid": grid,
        "placed_words": [],
        "word_bank": word_bank,
        "theme": "science and nature",
    }

    with open(out_dir / "db.json", "w") as f:
        json.dump(db, f, indent=2)
    print(f"Generated {len(word_bank)} words in word bank")


if __name__ == "__main__":
    main()
