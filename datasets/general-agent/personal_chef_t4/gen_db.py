recipes = []
recipe_id = 1
for cuisine, names in RECIPE_NAMES.items():
    for name in names:
        # Pick 3-6 random ingredients
        n_ings = random.randint(3, 6)
        ings = random.sample(INGREDIENT_NAMES, k=n_ings)
        # Determine dietary tags based on ingredients
        tags = set()
        has_meat = any(i in ["chicken", "beef", "shrimp", "salmon"] for i in ings)
        has_dairy = any(i in ["butter", "parmesan", "mozzarella", "feta", "cream"] for i in ings)
        has_nuts = any(i in ["peanuts", "cashews", "almonds", "pine nuts", "walnuts"] for i in ings)
        has_gluten = any(i in ["flour", "bread crumbs", "panko", "couscous", "pasta"] for i in ings)
        has_soy = any(i in ["soy sauce", "miso paste"] for i in ings)
        if not has_meat:
            tags.add("vegetarian")
        if not has_meat and not has_dairy:
            tags.add("vegan")
        if not has_gluten:
            tags.add("gluten-free")
        if not has_nuts:
            tags.add("nut-free")
        if not has_dairy:
            tags.add("dairy-free")
        if not has_soy:
            tags.add("soy-free")
        recipes.append(
            {
                "id": f"RC-{recipe_id:03d}",
                "name": name,
                "cuisine": cuisine,
                "ingredients": ings,
                "prep_time_minutes": random.randint(15, 90),
                "difficulty": random.choice(["easy", "medium", "hard"]),
                "dietary_tags": sorted(list(tags)),
                "calories_per_serving": random.randint(200, 700),
            }
        )
        recipe_id += 1

