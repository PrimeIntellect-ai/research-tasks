import os
import glob
import json
import sqlite3
import subprocess
from collections import Counter
import pytest

def test_networkx_fixed():
    """Test that networkx is fixed and installed correctly."""
    cmd = ['python3', '-c', 'import networkx as nx; g = nx.DiGraph(); g.add_node(1); print(g.nodes())']
    result = subprocess.run(cmd, capture_output=True, text=True)
    assert result.returncode == 0, f"Failed to import or use networkx. Error: {result.stderr}"
    assert "[1]" in result.stdout, "networkx did not behave as expected after fix."

def test_adversarial_corpus():
    """Test the agent's script against the evil and clean corpora."""
    script_path = "/home/user/validate_backup.py"
    assert os.path.isfile(script_path), f"Script {script_path} not found."

    evil_dir = "/home/user/corpus/evil"
    clean_dir = "/home/user/corpus/clean"

    evil_files = glob.glob(os.path.join(evil_dir, "*.json"))
    clean_files = glob.glob(os.path.join(clean_dir, "*.json"))

    assert len(evil_files) > 0, "No evil corpus files found."
    assert len(clean_files) > 0, "No clean corpus files found."

    db_path = "/home/user/backups.db"
    # Remove DB to ensure we only test the state generated by our test run
    if os.path.exists(db_path):
        os.remove(db_path)

    evil_bypassed = []
    for f in evil_files:
        result = subprocess.run(["python3", script_path, f], capture_output=True, text=True)
        if result.returncode != 1 or "REJECTED" not in result.stdout:
            evil_bypassed.append(os.path.basename(f))

    clean_failed = []
    for f in clean_files:
        result = subprocess.run(["python3", script_path, f], capture_output=True, text=True)
        if result.returncode != 0 or "ACCEPTED" not in result.stdout:
            clean_failed.append(os.path.basename(f))

    errors = []
    if evil_bypassed:
        errors.append(f"{len(evil_bypassed)} of {len(evil_files)} evil bypassed: {', '.join(evil_bypassed)}")
    if clean_failed:
        errors.append(f"{len(clean_failed)} of {len(clean_files)} clean modified (failed to accept): {', '.join(clean_failed)}")

    if errors:
        pytest.fail("; ".join(errors))

    # Verify SQLite export for clean files
    assert os.path.isfile(db_path), f"Database {db_path} was not created."

    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()

    cursor.execute("SELECT name FROM sqlite_master WHERE type='table' AND name='edges'")
    assert cursor.fetchone() is not None, "Table 'edges' does not exist in the database."

    cursor.execute("SELECT source, target FROM edges")
    db_edges = cursor.fetchall()
    conn.close()

    expected_edges = []
    for f in clean_files:
        with open(f, 'r') as jf:
            data = json.load(jf)
            for edge in data:
                expected_edges.append((str(edge['source']), str(edge['target'])))

    assert len(db_edges) == len(expected_edges), f"Expected {len(expected_edges)} edges in DB, found {len(db_edges)}."
    assert Counter(db_edges) == Counter(expected_edges), "Edges in database do not match the expected clean edges."