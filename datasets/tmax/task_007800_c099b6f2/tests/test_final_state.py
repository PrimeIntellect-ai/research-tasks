# test_final_state.py
import os
import subprocess
import json
import tempfile
import shutil

def test_provision_out_dir_exists():
    assert os.path.isdir("/home/user/provision_out"), "Directory /home/user/provision_out does not exist."

def test_git_repo_is_bare():
    repo_path = "/home/user/edge-config.git"
    assert os.path.isdir(repo_path), f"Git repository directory {repo_path} does not exist."
    res = subprocess.run(["git", "-C", repo_path, "rev-parse", "--is-bare-repository"], capture_output=True, text=True)
    assert res.stdout.strip() == "true", f"{repo_path} is not a bare git repository."

def test_post_receive_hook_executable():
    hook_path = "/home/user/edge-config.git/hooks/post-receive"
    assert os.path.isfile(hook_path), "post-receive hook does not exist."
    assert os.access(hook_path, os.X_OK), "post-receive hook is not executable."
    with open(hook_path, "r") as f:
        first_line = f.readline().strip()
    assert "python3" in first_line, "post-receive hook must use a python3 shebang."

def test_gitops_pipeline():
    temp_dir = tempfile.mkdtemp()
    try:
        # Clone the repo
        clone_res = subprocess.run(["git", "clone", "/home/user/edge-config.git", temp_dir], capture_output=True)
        assert clone_res.returncode == 0, "Failed to clone the bare repository."

        # Create router.json payload
        router_data = {
            "interfaces": {
                "eth0": {
                    "ip": "192.168.10.5/24",
                    "gateway": "192.168.10.1"
                },
                "eth1": {
                    "ip": "10.0.5.1/24"
                }
            },
            "port_forwarding": [
                {
                    "in_port": 80,
                    "out_ip": "10.0.5.100",
                    "out_port": 8080
                },
                {
                    "in_port": 443,
                    "out_ip": "10.0.5.101",
                    "out_port": 8443
                }
            ]
        }
        with open(os.path.join(temp_dir, "router.json"), "w") as f:
            json.dump(router_data, f)

        # Configure git and push
        subprocess.run(["git", "config", "user.email", "test@example.com"], cwd=temp_dir, check=True)
        subprocess.run(["git", "config", "user.name", "Test User"], cwd=temp_dir, check=True)
        subprocess.run(["git", "checkout", "-b", "master"], cwd=temp_dir, capture_output=True) # Ensure we are on master
        subprocess.run(["git", "add", "router.json"], cwd=temp_dir, check=True)
        subprocess.run(["git", "commit", "-m", "Test push"], cwd=temp_dir, check=True)

        push_res = subprocess.run(["git", "push", "origin", "master"], cwd=temp_dir, capture_output=True, text=True)
        assert push_res.returncode == 0, f"Git push failed: {push_res.stderr}"

        # Verify netplan.yaml
        netplan_path = "/home/user/provision_out/netplan.yaml"
        assert os.path.isfile(netplan_path), f"{netplan_path} was not generated by the hook."

        with open(netplan_path, "r") as f:
            netplan_content = f.read()

        # Validate essential elements in the YAML structure
        assert "network:" in netplan_content, "Missing 'network:' root key in netplan.yaml"
        assert "ethernets:" in netplan_content, "Missing 'ethernets:' key in netplan.yaml"
        assert "eth0:" in netplan_content, "Missing 'eth0:' interface in netplan.yaml"
        assert "192.168.10.5/24" in netplan_content, "Missing IP address for eth0 in netplan.yaml"
        assert "via: 192.168.10.1" in netplan_content, "Missing gateway route for eth0 in netplan.yaml"
        assert "eth1:" in netplan_content, "Missing 'eth1:' interface in netplan.yaml"
        assert "10.0.5.1/24" in netplan_content, "Missing IP address for eth1 in netplan.yaml"

        # Verify nftables.conf
        nftables_path = "/home/user/provision_out/nftables.conf"
        assert os.path.isfile(nftables_path), f"{nftables_path} was not generated by the hook."

        with open(nftables_path, "r") as f:
            nftables_lines = [line.strip() for line in f.readlines() if line.strip()]

        expected_lines = [
            "add rule ip nat PREROUTING tcp dport 80 counter dnat to 10.0.5.100:8080",
            "add rule ip nat PREROUTING tcp dport 443 counter dnat to 10.0.5.101:8443"
        ]

        assert nftables_lines == expected_lines, f"nftables.conf contents are incorrect. Expected {expected_lines}, got {nftables_lines}"

    finally:
        shutil.rmtree(temp_dir)