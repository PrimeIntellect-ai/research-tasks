# test_final_state.py

import os
import re
import stat
import pytest

def test_env_profile_exists_and_correct():
    """Ensure .env_profile exists and exports NETWORK_CONFIG_PATH correctly."""
    env_path = "/home/user/operator_project/.env_profile"
    assert os.path.isfile(env_path), f"File {env_path} does not exist."

    with open(env_path, "r") as f:
        content = f.read()

    # Allow for spaces around equals sign or quotes
    pattern = r"export\s+NETWORK_CONFIG_PATH\s*=\s*[\"']?/home/user/operator_project/network_config\.json[\"']?"
    assert re.search(pattern, content), f"{env_path} does not correctly export NETWORK_CONFIG_PATH."

def test_ci_cd_script_exists_and_executable():
    """Ensure ci_cd.sh exists, is executable, and contains required elements."""
    script_path = "/home/user/operator_project/ci_cd.sh"
    assert os.path.isfile(script_path), f"Script {script_path} does not exist."

    # Check if executable
    st = os.stat(script_path)
    assert bool(st.st_mode & stat.S_IXUSR), f"Script {script_path} is not executable."

    with open(script_path, "r") as f:
        content = f.read()

    assert "gcc" in content, f"Script {script_path} does not contain 'gcc'."
    assert re.search(r"\b(source|\.)\s+", content), f"Script {script_path} does not source the .env_profile."
    assert re.search(r"\b(for|while|until)\b", content), f"Script {script_path} does not contain a loop mechanism."

def test_operator_bin_exists_and_executable():
    """Ensure operator.bin was compiled and is executable."""
    bin_path = "/home/user/operator_project/operator.bin"
    assert os.path.isfile(bin_path), f"Executable {bin_path} does not exist."

    st = os.stat(bin_path)
    assert bool(st.st_mode & stat.S_IXUSR), f"Binary {bin_path} is not executable."

def test_deploy_log_exists_and_correct():
    """Ensure deploy.log was generated by the operator and contains the success message."""
    log_path = "/home/user/operator_project/deploy.log"
    assert os.path.isfile(log_path), f"Log file {log_path} does not exist. Did the operator run successfully?"

    with open(log_path, "r") as f:
        content = f.read()

    expected_content = "DEPLOY_SUCCESS: Read config successfully.\n"
    assert content == expected_content, f"Content of {log_path} is incorrect. Expected: {expected_content!r}, got: {content!r}"