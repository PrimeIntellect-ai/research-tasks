def test_sync_users_binary_exists():
    """Check that sync_users binary was built and placed correctly."""
    binary_path = "/home/user/bin/sync_users"
    assert os.path.isfile(binary_path), f"{binary_path} does not exist."
    assert os.access(binary_path, os.X_OK), f"{binary_path} is not executable."

def test_cron_file_exists():
    """Check that the crontab file exists and has the correct schedule."""
    cron_path = "/home/user/sync.cron"
    assert os.path.isfile(cron_path), f"{cron_path} does not exist."
    with open(cron_path, "r") as f:
        content = f.read()
    assert "* * * * *" in content, f"Expected '* * * * *' schedule in {cron_path}."

def test_json_output_correctness():
    """Check that sync_out.json exists, has 200000 records, and correct hashes."""
    json_path = "/home/user/sync_out.json"
    assert os.path.isfile(json_path), f"{json_path} does not exist."

    with open(json_path, "r") as f:
        try:
            data = json.load(f)
        except json.JSONDecodeError:
            pytest.fail(f"{json_path} is not valid JSON.")

    assert len(data) == 200000, f"Expected 200000 records, found {len(data)}."

    # Check a sample to verify correctness
    sample = data[0]
    assert "username" in sample, "Missing 'username' key in JSON objects."
    assert "role" in sample, "Missing 'role' key in JSON objects."
    assert "hash" in sample, "Missing 'hash' key in JSON objects."

    # Verify hash for the first record
    expected_hash = hashlib.sha256((sample["username"] + sample["role"]).encode('utf-8')).hexdigest()
    assert sample["hash"] == expected_hash, f"Incorrect hash for {sample['username']}. Expected {expected_hash}, got {sample['hash']}."

def test_performance_metric():
    """Check that the execution time of sync_users is under the 1.5s threshold."""
    binary_path = "/home/user/bin/sync_users"

    # Remove output file to ensure it's generated by this run
    json_path = "/home/user/sync_out.json"
    if os.path.exists(json_path):
        os.remove(json_path)

    start_time = time.time()
    result = subprocess.run([binary_path], capture_output=True)
    end_time = time.time()

    assert result.returncode == 0, f"sync_users failed with return code {result.returncode}. Stderr: {result.stderr.decode()}"

    elapsed_time = end_time - start_time
    threshold = 1.5

    assert elapsed_time <= threshold, f"Performance metric failed: took {elapsed_time:.3f} seconds (threshold: {threshold}s)"