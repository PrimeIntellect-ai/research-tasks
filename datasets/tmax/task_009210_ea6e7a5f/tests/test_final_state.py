import os
import subprocess

def test_bad_commit_hash():
    expected_file = "/tmp/expected_bad_commit.txt"
    actual_file = "/home/user/bad_commit.txt"

    assert os.path.exists(expected_file), f"Truth file {expected_file} is missing."
    assert os.path.exists(actual_file), f"Student file {actual_file} is missing."

    with open(expected_file, "r") as f:
        expected_hash = f.read().strip()

    with open(actual_file, "r") as f:
        actual_hash = f.read().strip()

    assert actual_hash == expected_hash, f"Bad commit hash is incorrect. Expected {expected_hash}, got {actual_hash}."

def test_fix_patch_and_build():
    patch_file = "/home/user/fix.patch"
    repo_dir = "/home/user/repo"

    assert os.path.exists(patch_file), f"Patch file {patch_file} is missing."

    # Reset the repository to ensure a clean state for applying the patch
    subprocess.run(["git", "reset", "--hard", "HEAD"], cwd=repo_dir, capture_output=True)
    subprocess.run(["git", "clean", "-fd"], cwd=repo_dir, capture_output=True)

    # Apply the patch
    # Try git apply first
    apply_res = subprocess.run(["git", "apply", patch_file], cwd=repo_dir, capture_output=True)
    if apply_res.returncode != 0:
        # Fallback to standard patch
        apply_res = subprocess.run(["patch", "-p1", "-i", patch_file], cwd=repo_dir, capture_output=True)
        if apply_res.returncode != 0:
            apply_res = subprocess.run(["patch", "-p0", "-i", patch_file], cwd=repo_dir, capture_output=True)

    assert apply_res.returncode == 0, f"Failed to apply the patch {patch_file}. Ensure it is a valid diff."

    # Run the build script with a timeout
    build_script = "./build.sh"
    run_res = subprocess.run(["timeout", "2", build_script], cwd=repo_dir, capture_output=True)
    assert run_res.returncode == 0, "build.sh failed or hung (timed out after 2 seconds)."

    # Verify the output
    output_file = os.path.join(repo_dir, "output.txt")
    assert os.path.exists(output_file), f"{output_file} was not generated by build.sh."

    with open(output_file, "r") as f:
        content = f.read()

    assert "Bug1" in content, "Expected 'Bug1' in output.txt."
    assert "Bug2" in content, "Expected 'Bug2' in output.txt."
    assert '"' not in content, "Expected no quotes in output.txt."