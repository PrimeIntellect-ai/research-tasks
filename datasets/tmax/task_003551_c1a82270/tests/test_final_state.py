# test_final_state.py
import os
import subprocess

def test_gsl_installed():
    """Verify that libgsl-dev is installed."""
    assert os.path.exists("/usr/include/gsl/gsl_rng.h"), "libgsl-dev does not appear to be installed (/usr/include/gsl/gsl_rng.h missing)."

def test_files_exist():
    """Verify that the required source, executable, and result files exist."""
    assert os.path.exists("/home/user/bootstrap.c"), "/home/user/bootstrap.c is missing."
    assert os.path.exists("/home/user/bootstrap"), "/home/user/bootstrap is missing."
    assert os.access("/home/user/bootstrap", os.X_OK), "/home/user/bootstrap is not executable."
    assert os.path.exists("/home/user/result.txt"), "/home/user/result.txt is missing."

def test_result_correct():
    """Verify that result.txt matches the expected output generated by the reference implementation."""
    reference_c = """#include <stdio.h>
#include <stdlib.h>
#include <math.h>
#include <gsl/gsl_rng.h>

int main() {
    FILE *f = fopen("/home/user/data.txt", "r");
    if (!f) return 1;
    double data[100];
    for (int i = 0; i < 100; i++) {
        if (fscanf(f, "%lf", &data[i]) != 1) break;
    }
    fclose(f);

    gsl_rng *r = gsl_rng_alloc(gsl_rng_mt19937);
    gsl_rng_set(r, 42);

    int B = 1000;
    int N = 100;
    double means[1000];
    double sum_of_means = 0.0;

    for (int i = 0; i < B; i++) {
        double sample_sum = 0.0;
        for (int j = 0; j < N; j++) {
            int idx = gsl_rng_uniform_int(r, N);
            sample_sum += data[idx];
        }
        means[i] = sample_sum / N;
        sum_of_means += means[i];
    }

    double overall_mean = sum_of_means / B;
    double sum_sq_diff = 0.0;
    for (int i = 0; i < B; i++) {
        sum_sq_diff += (means[i] - overall_mean) * (means[i] - overall_mean);
    }
    double stddev = sqrt(sum_sq_diff / (B - 1));

    printf("Overall Mean: %.6f\\n", overall_mean);
    printf("StdDev: %.6f\\n", stddev);

    gsl_rng_free(r);
    return 0;
}
"""
    with open("/tmp/reference.c", "w") as f:
        f.write(reference_c)

    compile_cmd = ["gcc", "-o", "/tmp/reference", "/tmp/reference.c", "-lgsl", "-lgslcblas", "-lm"]
    compile_res = subprocess.run(compile_cmd, capture_output=True)
    assert compile_res.returncode == 0, f"Failed to compile reference code: {compile_res.stderr.decode()}"

    run_res = subprocess.run(["/tmp/reference"], capture_output=True, text=True)
    assert run_res.returncode == 0, "Failed to run reference code."

    expected_output = run_res.stdout.strip()

    with open("/home/user/result.txt", "r") as f:
        actual_output = f.read().strip()

    assert actual_output == expected_output, f"Expected result.txt to be:\n{expected_output}\n\nBut got:\n{actual_output}"