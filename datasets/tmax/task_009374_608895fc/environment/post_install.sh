syntax = "proto3";

package processor;

service TextProcessor {
  rpc ProcessText (ProcessRequest) returns (ProcessResponse) {}
}

enum Action {
  UPPER = 0;
  REVERSE = 1;
}

message ProcessRequest {
  string text = 1;
  Action action = 2;
}

message ProcessResponse {
  string result = 1;
}
EOF

cat << 'EOF' > /home/user/app/backend.py
import grpc
from concurrent import futures
import time

# These will be generated by the agent
import processor_pb2
import processor_pb2_grpc

class TextProcessorServicer(processor_pb2_grpc.TextProcessorServicer):
    def ProcessText(self, request, context):
        if request.action == processor_pb2.UPPER:
            res = request.text.upper()
        elif request.action == processor_pb2.REVERSE:
            res = request.text[::-1]
        else:
            res = request.text
        return processor_pb2.ProcessResponse(result=res)

def serve():
    server = grpc.server(futures.ThreadPoolExecutor(max_workers=10))
    processor_pb2_grpc.add_TextProcessorServicer_to_server(TextProcessorServicer(), server)
    server.add_insecure_port('[::]:50051')
    server.start()
    try:
        while True:
            time.sleep(86400)
    except KeyboardInterrupt:
        server.stop(0)

if __name__ == '__main__':
    serve()
EOF

