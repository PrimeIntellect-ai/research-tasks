import os
import subprocess
import random
import pytest

def test_historical_events_extracted():
    events_path = "/home/user/historical_events.txt"
    assert os.path.isfile(events_path), f"Missing extracted events file: {events_path}"

    expected_events = [
        "T 1 BACKUP",
        "T 2 USER",
        "T 3 USER",
        "W 2 1",
        "W 3 2",
        "W 1 3",
        "T 4 BACKUP",
        "W 4 3",
        "T 5 USER",
        "W 5 4",
        "W 2 4",
        "W 4 2",
        "R 5 4",
        "T 6 BACKUP",
        "W 6 6"
    ]

    with open(events_path, "r") as f:
        content = f.read().strip().splitlines()

    # Trim trailing/leading whitespaces from lines if any
    content = [line.strip() for line in content if line.strip()]

    assert content == expected_events, "The extracted historical events do not match the expected events from the video."

def test_resolution_log():
    log_path = "/home/user/resolution_log.txt"
    assert os.path.isfile(log_path), f"Missing resolution log file: {log_path}"

    events_path = "/home/user/historical_events.txt"
    oracle_path = "/opt/oracle/resolve_deadlocks_oracle"

    assert os.path.isfile(oracle_path), f"Missing oracle executable: {oracle_path}"

    with open(events_path, "r") as f:
        input_data = f.read()

    oracle_proc = subprocess.run([oracle_path], input=input_data, text=True, capture_output=True)
    expected_output = oracle_proc.stdout.strip()

    with open(log_path, "r") as f:
        actual_output = f.read().strip()

    assert actual_output == expected_output, "The resolution log output does not match the expected output generated by the oracle."

def test_fuzz_equivalence():
    agent_path = "/home/user/resolve_deadlocks"
    oracle_path = "/opt/oracle/resolve_deadlocks_oracle"

    assert os.path.isfile(agent_path), f"Missing agent executable: {agent_path}"
    assert os.access(agent_path, os.X_OK), f"Agent executable is not executable: {agent_path}"

    random.seed(42)
    iterations = 1000

    for i in range(iterations):
        num_lines = random.randint(10, 500)
        lines = []
        for _ in range(num_lines):
            event_type = random.choice(["T", "W", "R"])
            if event_type == "T":
                lines.append(f"T {random.randint(1, 10)} {random.choice(['USER', 'BACKUP'])}")
            elif event_type == "W":
                lines.append(f"W {random.randint(1, 10)} {random.randint(1, 10)}")
            elif event_type == "R":
                lines.append(f"R {random.randint(1, 10)} {random.randint(1, 10)}")

        input_data = "\n".join(lines) + "\n"

        oracle_proc = subprocess.run([oracle_path], input=input_data, text=True, capture_output=True)
        agent_proc = subprocess.run([agent_path], input=input_data, text=True, capture_output=True)

        assert oracle_proc.returncode == 0, f"Oracle failed on iteration {i} with input:\n{input_data}"

        if oracle_proc.stdout != agent_proc.stdout:
            pytest.fail(
                f"Fuzz equivalence failed on iteration {i}.\n"
                f"Input:\n{input_data}\n"
                f"Expected (Oracle):\n{oracle_proc.stdout}\n"
                f"Actual (Agent):\n{agent_proc.stdout}\n"
            )