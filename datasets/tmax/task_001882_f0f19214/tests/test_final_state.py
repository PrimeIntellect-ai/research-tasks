# test_final_state.py

import os
import subprocess
import pytest
import glob
import re

DETECT_SCRIPT = "/home/user/detect_anomaly.sh"
BENCH_SCRIPT = "/home/user/bootstrap_bench.sh"
BENCH_LOG = "/home/user/bench.log"
CLEAN_CORPUS = "/app/corpus/clean"
EVIL_CORPUS = "/app/corpus/evil"

def test_detect_anomaly_exists_and_executable():
    assert os.path.isfile(DETECT_SCRIPT), f"{DETECT_SCRIPT} does not exist."
    assert os.access(DETECT_SCRIPT, os.X_OK), f"{DETECT_SCRIPT} is not executable."

def test_bootstrap_bench_exists_and_executable():
    assert os.path.isfile(BENCH_SCRIPT), f"{BENCH_SCRIPT} does not exist."
    assert os.access(BENCH_SCRIPT, os.X_OK), f"{BENCH_SCRIPT} is not executable."

def test_adversarial_corpus():
    clean_files = glob.glob(os.path.join(CLEAN_CORPUS, "*.json"))
    evil_files = glob.glob(os.path.join(EVIL_CORPUS, "*.json"))

    assert len(clean_files) > 0, f"No clean JSON files found in {CLEAN_CORPUS}."
    assert len(evil_files) > 0, f"No evil JSON files found in {EVIL_CORPUS}."

    bypassed_evil = []
    modified_clean = []

    for f in evil_files:
        res = subprocess.run([DETECT_SCRIPT, f], capture_output=True)
        # Evil files should be rejected (exit code 1)
        if res.returncode != 1:
            bypassed_evil.append(os.path.basename(f))

    for f in clean_files:
        res = subprocess.run([DETECT_SCRIPT, f], capture_output=True)
        # Clean files should be preserved/accepted (exit code 0)
        if res.returncode != 0:
            modified_clean.append(os.path.basename(f))

    error_msg = []
    if bypassed_evil:
        error_msg.append(f"{len(bypassed_evil)} of {len(evil_files)} evil bypassed: {', '.join(bypassed_evil)}")
    if modified_clean:
        error_msg.append(f"{len(modified_clean)} of {len(clean_files)} clean modified (rejected): {', '.join(modified_clean)}")

    if error_msg:
        pytest.fail(" | ".join(error_msg))

def test_bootstrap_bench_execution():
    if os.path.exists(BENCH_LOG):
        os.remove(BENCH_LOG)

    # Run the bootstrap bench script, setting cwd to /home/user so `./detect_anomaly.sh` resolves correctly
    res = subprocess.run(
        [BENCH_SCRIPT, CLEAN_CORPUS], 
        cwd="/home/user", 
        capture_output=True,
        text=True
    )
    assert res.returncode == 0, f"{BENCH_SCRIPT} failed with exit code {res.returncode}. Stderr: {res.stderr}"

    assert os.path.isfile(BENCH_LOG), f"{BENCH_LOG} was not generated by {BENCH_SCRIPT}."

    with open(BENCH_LOG, "r") as f:
        content = f.read().strip()

    assert re.match(r'^\d+(\.\d+)?$', content), f"Expected a numeric value (milliseconds) in {BENCH_LOG}, but got: '{content}'"