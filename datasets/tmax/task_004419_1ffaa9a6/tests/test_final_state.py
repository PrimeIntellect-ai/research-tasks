import os
import subprocess
import pytest

def test_cwe_txt():
    path = "/home/user/cwe.txt"
    assert os.path.isfile(path), f"File {path} does not exist."
    with open(path, "r") as f:
        content = f.read().strip()
    assert "CWE-214" in content.upper(), f"Expected CWE-214 in {path}, but found: {content}"

def test_decrypted_secret():
    path = "/home/user/decrypted_secret.txt"
    assert os.path.isfile(path), f"File {path} does not exist."
    with open(path, "r") as f:
        content = f.read().strip()
    assert "FLAG{proc_cmdline_leaks_are_dangerous}" in content, f"Decrypted secret is incorrect."

def test_secure_backup_script():
    script_path = "/home/user/secure_backup.sh"
    assert os.path.isfile(script_path), f"File {script_path} does not exist."
    assert os.access(script_path, os.X_OK), f"File {script_path} is not executable."

    with open(script_path, "r") as f:
        script_content = f.read()

    # Check for insecure usage
    assert "pass:" not in script_content, f"The script {script_path} still contains insecure 'pass:' argument."

    # Test execution
    input_file = "/tmp/test_in.txt"
    output_file = "/tmp/test_out.enc"
    test_text = "test_encryption_data_123"

    with open(input_file, "w") as f:
        f.write(test_text)

    if os.path.exists(output_file):
        os.remove(output_file)

    env = os.environ.copy()
    env["BACKUP_PASS"] = "testpass"

    try:
        subprocess.run([script_path, input_file, output_file], env=env, check=True, capture_output=True)
    except subprocess.CalledProcessError as e:
        pytest.fail(f"Execution of {script_path} failed: {e.stderr.decode()}")

    assert os.path.isfile(output_file), f"The script did not create the output file at {output_file}."

    # Verify decryption
    try:
        decrypted = subprocess.check_output([
            "openssl", "enc", "-d", "-aes-256-cbc", "-pbkdf2", 
            "-in", output_file, "-pass", "pass:testpass"
        ], stderr=subprocess.STDOUT)
        assert test_text in decrypted.decode(), "Decrypted content does not match the original input."
    except subprocess.CalledProcessError as e:
        pytest.fail(f"Failed to decrypt the output file generated by {script_path}: {e.output.decode()}")