# test_final_state.py

import os
import json
import subprocess
import pytest

SCRIPT_PATH = "/home/user/etl_process.sh"
PROD_JSON_PATH = "/home/user/prod_graph.json"
DEV_JSON_PATH = "/home/user/dev_graph.json"

def test_script_exists_and_executable():
    assert os.path.exists(SCRIPT_PATH), f"Script {SCRIPT_PATH} does not exist."
    assert os.path.isfile(SCRIPT_PATH), f"{SCRIPT_PATH} is not a file."
    assert os.access(SCRIPT_PATH, os.X_OK), f"Script {SCRIPT_PATH} is not executable."

def test_prod_graph_json_contents():
    assert os.path.exists(PROD_JSON_PATH), f"Output file {PROD_JSON_PATH} does not exist. Did you run the script for the 'prod' environment?"

    with open(PROD_JSON_PATH, 'r') as f:
        try:
            data = json.load(f)
        except json.JSONDecodeError:
            pytest.fail(f"File {PROD_JSON_PATH} does not contain valid JSON.")

    expected = {
        "db-prod-01": {
            "outgoing_count": 1,
            "targets": ["cache-prod-01"]
        },
        "web-prod-01": {
            "outgoing_count": 2,
            "targets": ["db-prod-01", "web-dev-01"]
        }
    }

    assert data == expected, f"Contents of {PROD_JSON_PATH} do not match the expected graph output for the 'prod' environment."

def test_script_dynamic_execution():
    """
    Tests if the script correctly accepts an environment argument and processes
    the database correctly for a different environment ('dev').
    """
    if not os.path.exists(SCRIPT_PATH) or not os.access(SCRIPT_PATH, os.X_OK):
        pytest.skip("Script is missing or not executable.")

    # Run script for dev environment
    result = subprocess.run([SCRIPT_PATH, "dev"], capture_output=True, text=True)
    assert result.returncode == 0, f"Script failed when executed with 'dev' argument. Stderr: {result.stderr}"

    assert os.path.exists(DEV_JSON_PATH), f"Script did not generate {DEV_JSON_PATH} when run with 'dev' argument."

    with open(DEV_JSON_PATH, 'r') as f:
        try:
            data = json.load(f)
        except json.JSONDecodeError:
            pytest.fail(f"File {DEV_JSON_PATH} generated by the script does not contain valid JSON.")

    expected = {
        "web-dev-01": {
            "outgoing_count": 1,
            "targets": ["db-prod-01"]
        }
    }

    assert data == expected, f"Contents of {DEV_JSON_PATH} do not match the expected graph output for the 'dev' environment."