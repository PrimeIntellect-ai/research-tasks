# test_final_state.py

import os
import json
import time
import subprocess
import shutil
import tempfile
import pytest

def test_fast_oracle_correctness_and_speedup():
    fast_oracle_path = "/home/user/fast_oracle.py"
    assert os.path.isfile(fast_oracle_path), f"Missing fast_oracle.py at {fast_oracle_path}"

    authors_csv = "/home/user/data/authors.csv"
    papers_dir = "/home/user/data/papers"
    queries_txt = "/home/user/queries.txt"
    fast_out = "/home/user/fast_results.json"

    assert os.path.isfile(authors_csv), "authors.csv missing"
    assert os.path.isdir(papers_dir), "papers directory missing"
    assert os.path.isfile(queries_txt), "queries.txt missing"

    with tempfile.TemporaryDirectory() as tmpdir:
        # Run the original oracle
        oracle_start = time.time()
        # Run in tmpdir to capture any hardcoded output file like 'results.json'
        subprocess.run(
            ["/app/citation_oracle", authors_csv, papers_dir, queries_txt],
            cwd=tmpdir,
            check=True
        )
        oracle_time = time.time() - oracle_start

        # Find the output file generated by the oracle
        oracle_out_files = [f for f in os.listdir(tmpdir) if f.endswith(".json")]
        assert len(oracle_out_files) == 1, "Could not identify the oracle's output JSON file."
        oracle_out_path = os.path.join(tmpdir, oracle_out_files[0])

        with open(oracle_out_path, "r") as f:
            oracle_data = json.load(f)

    # Clean up previous fast_results.json if it exists
    if os.path.exists(fast_out):
        os.remove(fast_out)

    # Run the fast oracle
    fast_start = time.time()
    subprocess.run(
        ["python3", fast_oracle_path, authors_csv, papers_dir, queries_txt],
        check=True
    )
    fast_time = time.time() - fast_start

    assert os.path.isfile(fast_out), f"Fast oracle did not produce the expected output file at {fast_out}"

    with open(fast_out, "r") as f:
        fast_data = json.load(f)

    # 1. Output equality
    assert oracle_data == fast_data, "The output of fast_oracle.py does not exactly match the original oracle."

    # 2. Speedup metric
    speedup = oracle_time / fast_time if fast_time > 0 else float('inf')
    assert speedup >= 10.0, f"Speedup is {speedup:.2f}x (Oracle: {oracle_time:.2f}s, Fast: {fast_time:.2f}s). Expected >= 10.0x."