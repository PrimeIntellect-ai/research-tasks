apt-get update && apt-get install -y python3 python3-pip gcc tar zip unzip binutils coreutils
    pip3 install pytest

    useradd -m -s /bin/bash user || true

    mkdir -p /home/user/incoming_artifacts/batch1
    mkdir -p /home/user/incoming_artifacts/batch2

    # 1. Create a valid ELF x86_64 archive
    cat << 'EOF' > /tmp/main.c
int main() { return 0; }
EOF
    gcc /tmp/main.c -o /tmp/x86_bin
    tar -czf /home/user/incoming_artifacts/batch1/app_x86.tar.gz -C /tmp x86_bin
    rm /tmp/main.c /tmp/x86_bin

    # 2. Create a valid ELF ARM archive
    cat << 'EOF' | base64 -d > /tmp/arm_bin
f0VMRgEBAQAAAAAAAAAAAAIACAABAAAAVIAAAABAAAAAAAAAAAAAADQAIAABAAAAAAAAAAEAAAAAAAAAAABAAAAAQAAAAEAAAAHAAAAACAAAAAAAAABQAAAAEAAABIAAAASAAAAEgAAAAgAAAAIAAAACgAAAAEAAAAEAAAAAwAAAAgAAAAIAAAACAAAAAAQAAAAAAAAAAAAAAAEAAAAAAAAAAQAAAA==
EOF
    chmod +x /tmp/arm_bin
    cd /tmp && zip /home/user/incoming_artifacts/batch1/firmware_arm.zip arm_bin
    rm /tmp/arm_bin

    # 3. Create a valid GCode archive
    cat << 'EOF' > /tmp/model.gcode
; generated by Slic3r
; filament_type = ABS
G28 ; home all axes
G1 Z5 F5000 ; lift nozzle
EOF
    tar -czf /home/user/incoming_artifacts/batch2/model_abs.tar.gz -C /tmp model.gcode
    rm /tmp/model.gcode

    # 4. Create a corrupt tar.gz
    head -c 100 /dev/urandom > /home/user/incoming_artifacts/batch2/corrupt_data.tar.gz

    # 5. Create a corrupt zip
    head -c 100 /dev/urandom > /home/user/incoming_artifacts/batch1/bad_archive.zip

    chmod -R 777 /home/user