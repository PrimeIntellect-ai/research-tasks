import os
import subprocess
import pytest

def test_regression_report():
    report_path = "/home/user/regression_report.txt"
    secret_path = "/home/user/.secret/expected_commits.txt"

    assert os.path.isfile(report_path), f"Report file {report_path} does not exist. You must create it with the bisected commit hashes."

    with open(report_path, "r") as f:
        report_content = f.read().strip()

    with open(secret_path, "r") as f:
        secret_content = f.read().strip()

    assert report_content == secret_content, f"The regression report content is incorrect. Expected exactly what is in the secret file."

def test_pipeline_execution_and_results():
    pipeline_dir = "/home/user/data_pipeline"
    pipeline_script = os.path.join(pipeline_dir, "pipeline.sh")

    assert os.path.isfile(pipeline_script), "pipeline.sh is missing."

    # Run the pipeline to ensure it doesn't crash and generates the expected output
    result = subprocess.run(["./pipeline.sh"], cwd=pipeline_dir, capture_output=True, text=True)
    assert result.returncode == 0, f"pipeline.sh failed with exit code {result.returncode}. It might still be crashing (core dump).\nstderr: {result.stderr}"

    results_file = os.path.join(pipeline_dir, "results.txt")
    assert os.path.isfile(results_file), "results.txt was not generated by pipeline.sh."

    with open(results_file, "r") as f:
        lines = f.read().splitlines()

    assert len(lines) == 100, f"Expected exactly 100 lines in results.txt, found {len(lines)}. The race condition may not be fully resolved."

    expected_lines = {f"Processed Data: {i}" for i in range(1, 101)}
    actual_lines = set(lines)

    missing_lines = expected_lines - actual_lines
    extra_lines = actual_lines - expected_lines

    assert not missing_lines and not extra_lines, (
        f"The content of results.txt does not match the expected output.\n"
        f"Missing: {list(missing_lines)[:5]}...\n"
        f"Unexpected: {list(extra_lines)[:5]}..."
    )