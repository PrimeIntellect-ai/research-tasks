# test_final_state.py

import os
import tarfile
import pytest

def test_extracted_artifacts_exists():
    """Test that the final extracted_artifacts.tar.gz archive exists."""
    path = "/home/user/extracted_artifacts.tar.gz"
    assert os.path.isfile(path), f"File {path} does not exist. The output archive was not generated."

def test_extracted_artifacts_contents():
    """Test that the archive contains exactly the expected files with correct names and flat structure."""
    path = "/home/user/extracted_artifacts.tar.gz"
    if not os.path.isfile(path):
        pytest.skip("Archive not found, skipping content test.")

    # Read expected hashes generated by the setup script
    try:
        with open("/tmp/hash_elf1", "r") as f:
            elf1_hash = f.read().strip()
        with open("/tmp/hash_elf2", "r") as f:
            elf2_hash = f.read().strip()
        with open("/tmp/hash_wal1", "r") as f:
            wal1_hash = f.read().strip()
    except FileNotFoundError as e:
        pytest.fail(f"Could not read expected hash files: {e}")

    expected_names = {
        f"{elf1_hash}.elf",
        f"{elf2_hash}.elf",
        f"{wal1_hash}.wal"
    }

    with tarfile.open(path, "r:gz") as tar:
        members = tar.getmembers()

        # Ensure only files are present (no directories)
        for m in members:
            assert m.isfile(), f"Archive contains non-file member: {m.name}. It should only contain files."

        # Ensure flat structure
        for m in members:
            name = m.name
            if name.startswith("./"):
                name = name[2:]
            assert "/" not in name, f"File '{m.name}' is not in the root of the archive. The archive must be flat."

        actual_names = set(m.name.split("/")[-1] for m in members)

        assert len(actual_names) == 3, f"Expected exactly 3 files in the archive, found {len(actual_names)}: {actual_names}"
        assert actual_names == expected_names, f"Archive contents mismatch. Expected {expected_names}, found {actual_names}"