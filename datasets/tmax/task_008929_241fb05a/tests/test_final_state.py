import os
import json
import subprocess
import pytest

def test_config_file_created():
    config_path = "/tmp/math_config.json"
    assert os.path.isfile(config_path), f"Config file {config_path} was not created."

    with open(config_path, "r") as f:
        try:
            data = json.load(f)
        except json.JSONDecodeError:
            pytest.fail(f"{config_path} does not contain valid JSON.")

    assert data.get("threads") == 4, f"Config file {config_path} does not contain {{'threads': 4}}."

def test_bad_commit_identified():
    actual_path = "/home/user/bad_commit.txt"
    expected_path = "/tmp/expected_bad_commit.txt"

    assert os.path.isfile(actual_path), f"File {actual_path} was not created."
    assert os.path.isfile(expected_path), f"Expected bad commit file {expected_path} is missing."

    with open(actual_path, "r") as f:
        actual_bad = f.read().strip()

    with open(expected_path, "r") as f:
        expected_bad = f.read().strip()

    assert actual_bad == expected_bad, f"Incorrect bad commit identified. Expected {expected_bad}, got {actual_bad}."

def test_result_file_correct():
    result_path = "/home/user/result.txt"
    assert os.path.isfile(result_path), f"Result file {result_path} was not created."

    with open(result_path, "r") as f:
        result = f.read().strip()

    expected_result = "333333383333335000000"
    assert expected_result in result, f"Result file does not contain the correct mathematical output. Expected {expected_result}."

def test_cmake_conflict_resolved():
    cmake_path = "/home/user/math_sim/CMakeLists.txt"
    assert os.path.isfile(cmake_path), f"CMakeLists.txt is missing at {cmake_path}."

    with open(cmake_path, "r") as f:
        content = f.read()

    assert "Boost 1.99.0" not in content, "CMakeLists.txt still contains the fictional Boost dependency conflict."

def test_project_builds_and_runs_correctly():
    repo_path = "/home/user/math_sim"

    # Clean and build
    build_cmd = "cmake . && make"
    build_result = subprocess.run(build_cmd, shell=True, cwd=repo_path, capture_output=True, text=True)
    assert build_result.returncode == 0, f"Project failed to build:\n{build_result.stderr}"

    exec_path = os.path.join(repo_path, "math_sim")
    assert os.path.isfile(exec_path), "Executable 'math_sim' was not generated by the build process."

    # Run multiple times to ensure the race condition is fixed
    for _ in range(5):
        run_result = subprocess.run([exec_path], cwd=repo_path, capture_output=True, text=True)
        assert run_result.returncode == 0, f"Program failed to run:\n{run_result.stderr}"
        output = run_result.stdout.strip()
        assert "333333383333335000000" in output, f"Program output incorrect (race condition might still exist). Got: {output}"