def test_rolling_impact_csv():
    csv_file = "/home/user/rolling_impact.csv"
    db_file = "/home/user/system.db"

    assert os.path.isfile(csv_file), f"{csv_file} is missing."
    assert os.path.isfile(db_file), f"{db_file} is missing."

    # Compute expected results from the database
    conn = sqlite3.connect(db_file)
    cursor = conn.cursor()

    # The agent was supposed to repair the index, but we can just query the data
    # to compute the expected rolling sum.
    query = """
        SELECT service_id, event_time, 
               SUM(impact_score) OVER (
                   PARTITION BY service_id 
                   ORDER BY event_time 
                   ROWS BETWEEN 2 PRECEDING AND CURRENT ROW
               ) as rolling_score
        FROM events
    """
    try:
        cursor.execute(query)
        expected_rows = cursor.fetchall()
    except sqlite3.DatabaseError as e:
        pytest.fail(f"Database error when querying system.db (was the index repaired?): {e}")
    finally:
        conn.close()

    expected_set = {(str(row[0]), str(row[1]), str(row[2])) for row in expected_rows}

    # Read the CSV generated by the agent
    actual_set = set()
    with open(csv_file, "r", newline="") as f:
        reader = csv.reader(f)
        header = next(reader, None)
        if header and header[0] == "service_id":
            pass # Skip header if present
        else:
            if header:
                actual_set.add((str(header[0]), str(header[1]), str(header[2])))
        for row in reader:
            if len(row) >= 3:
                actual_set.add((str(row[0]), str(row[1]), str(row[2])))

    assert actual_set == expected_set, "The rolling_impact.csv contents do not match the expected window function results."

def test_sqlsanitize_adversarial_corpus():
    tool_path = "/home/user/sanitizer/sqlsanitize"
    evil_corpus = "/app/corpus/evil.txt"
    clean_corpus = "/app/corpus/clean.txt"

    assert os.path.isfile(tool_path), f"Sanitizer tool {tool_path} is missing."
    assert os.access(tool_path, os.X_OK), f"Sanitizer tool {tool_path} is not executable."

    # Test Evil Corpus
    result_evil = subprocess.run([tool_path, evil_corpus], capture_output=True, text=True)
    assert result_evil.returncode == 0, f"sqlsanitize failed on evil corpus with exit code {result_evil.returncode}."

    evil_output = result_evil.stdout.strip()
    assert evil_output == "", f"sqlsanitize failed to reject all evil queries. Output was not empty:\n{evil_output}"

    # Test Clean Corpus
    result_clean = subprocess.run([tool_path, clean_corpus], capture_output=True, text=True)
    assert result_clean.returncode == 0, f"sqlsanitize failed on clean corpus with exit code {result_clean.returncode}."

    with open(clean_corpus, "r") as f:
        expected_clean_output = f.read().strip()

    actual_clean_output = result_clean.stdout.strip()

    assert actual_clean_output == expected_clean_output, "sqlsanitize modified or rejected clean queries."