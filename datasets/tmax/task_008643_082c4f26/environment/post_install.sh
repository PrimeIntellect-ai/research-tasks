    mkdir -p /home/user/audit
    cd /home/user/audit

    # 1. Secret Key
    echo -n "AuditSecretKey2024!" > secret.key

    # 2. Generate proxy.crt
    openssl req -x509 -nodes -days 365 -newkey rsa:2048 -keyout proxy.key -out proxy.crt -subj "/C=US/ST=State/L=City/O=Organization/CN=data-proxy.internal.corp" -addext "subjectAltName=DNS:data-proxy.internal.corp" 2>/dev/null
    CERT_EXP=$(openssl x509 -in proxy.crt -noout -enddate | cut -d= -f2)
    CERT_EXP_ISO=$(date -d "$CERT_EXP" -u +"%Y-%m-%dT%H:%M:%SZ")
    echo "$CERT_EXP_ISO" > /tmp/expected_cert_exp.txt

    # 3. Create logs.json
    cat << 'EOF' > generate_logs.py
import json, hashlib, hmac

secret = b"AuditSecretKey2024!"

def create_entry(id_val, payload, corrupt_hash=False, corrupt_token=False):
    h = hashlib.sha256(payload.encode()).hexdigest()
    if corrupt_hash:
        h = hashlib.sha256((payload+"bad").encode()).hexdigest()

    msg = f"{id_val}:{payload}".encode()
    t = hmac.new(secret, msg, hashlib.sha256).hexdigest()
    if corrupt_token:
        t = hmac.new(secret, msg + b"bad", hashlib.sha256).hexdigest()

    return {"id": id_val, "payload": payload, "checksum": h, "token": t}

logs = [
    create_entry("log-001", "system start", False, False),
    create_entry("log-002", "user login", True, False),     # bad hash
    create_entry("log-003", "data export", False, True),    # bad token
    create_entry("log-004", "admin logout", True, True),    # both bad
    create_entry("log-005", "system shutdown", False, False)
]

with open("logs.json", "w") as f:
    json.dump(logs, f, indent=2)
EOF
    python3 generate_logs.py
    rm generate_logs.py

