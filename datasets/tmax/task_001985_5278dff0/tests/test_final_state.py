import os
import math

def test_alpha_ci_exists_and_format():
    output_file = "/home/user/alpha_ci.txt"
    assert os.path.exists(output_file), f"Output file {output_file} does not exist. The Go program may not have run successfully or did not write to the correct path."

    with open(output_file, "r") as f:
        content = f.read().strip()

    parts = content.split(",")
    assert len(parts) == 2, f"Expected exactly 2 comma-separated values in {output_file}, but got: {content}"

    try:
        lower = float(parts[0])
        upper = float(parts[1])
    except ValueError:
        assert False, f"Could not parse the values in {output_file} as floats. Content: {content}"

def test_alpha_ci_values():
    output_file = "/home/user/alpha_ci.txt"
    assert os.path.exists(output_file), f"Output file {output_file} does not exist."

    with open(output_file, "r") as f:
        content = f.read().strip()

    parts = content.split(",")
    assert len(parts) == 2, "Output format is incorrect."

    lower = float(parts[0])
    upper = float(parts[1])

    # Expected bounds generated by the reference Go script using Go's math/rand PRNG
    expected_lower = 3.154964
    expected_upper = 3.751280

    assert math.isclose(lower, expected_lower, abs_tol=5e-5), \
        f"Lower bound {lower} is not within tolerance of expected {expected_lower}. Check your Newton-Raphson implementation or PRNG logic."

    assert math.isclose(upper, expected_upper, abs_tol=5e-5), \
        f"Upper bound {upper} is not within tolerance of expected {expected_upper}. Check your Newton-Raphson implementation or PRNG logic."