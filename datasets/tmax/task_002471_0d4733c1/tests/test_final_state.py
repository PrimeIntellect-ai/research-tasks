def test_health_endpoint():
    ctx = ssl.create_default_context()
    ctx.check_hostname = False
    ctx.verify_mode = ssl.CERT_NONE

    req = urllib.request.Request("https://127.0.0.1:8443/health")
    try:
        with urllib.request.urlopen(req, context=ctx, timeout=5) as response:
            assert response.status == 200, f"Expected 200 OK, got {response.status}"
            data = json.loads(response.read().decode('utf-8'))
            assert data.get("status") == "ok", "Expected status: ok"
            assert data.get("redis") == "connected", "Expected redis: connected"
    except urllib.error.URLError as e:
        pytest.fail(f"Failed to connect to /health endpoint: {e}")

def test_ingest_endpoint_clean():
    ctx = ssl.create_default_context()
    ctx.check_hostname = False
    ctx.verify_mode = ssl.CERT_NONE

    clean_file = "/home/user/corpus/clean/test1.json"
    if not os.path.exists(clean_file):
        clean_files = [f for f in os.listdir("/home/user/corpus/clean") if f.endswith('.json')]
        if clean_files:
            clean_file = os.path.join("/home/user/corpus/clean", clean_files[0])
        else:
            pytest.skip("No clean files available to test ingest endpoint")

    with open(clean_file, 'rb') as f:
        payload = f.read()

    req = urllib.request.Request(
        "https://127.0.0.1:8443/ingest",
        data=payload,
        headers={"Content-Type": "application/json"}
    )

    try:
        with urllib.request.urlopen(req, context=ctx, timeout=5) as response:
            assert response.status == 200, f"Expected 200 OK for clean payload, got {response.status}"
    except urllib.error.HTTPError as e:
        pytest.fail(f"Clean payload rejected by ingest endpoint with status {e.code}")
    except urllib.error.URLError as e:
        pytest.fail(f"Failed to connect to /ingest endpoint: {e}")

