# test_final_state.py

import os
import subprocess
import pytest

def generate_test_data():
    """Generates synthetic raw logs and their expected redacted counterparts."""
    raw_lines = [
        "User login from 192.168.1.100 successful.",
        "Payment processed with card 1234-5678-9012-3456.",
        "API token a1b2c3d4e5f6a1b2c3d4e5f6a1b2c3d4 used.",
        "Normal log entry without sensitive data.",
        "Another IP 10.0.0.1 connected.",
        "Invalid CC 123-456-789 is ignored.", 
        "Short hex a1b2c3 is ignored.",
        "Edge case IP 255.255.255.255.",
        "Token 00000000000000000000000000000000 accepted."
    ] * 120 # Generates 1080 lines

    expected_lines = [
        "User login from [REDACTED] successful.",
        "Payment processed with card [REDACTED].",
        "API token [REDACTED] used.",
        "Normal log entry without sensitive data.",
        "Another IP [REDACTED] connected.",
        "Invalid CC 123-456-789 is ignored.",
        "Short hex a1b2c3 is ignored.",
        "Edge case IP [REDACTED].",
        "Token [REDACTED] accepted."
    ] * 120

    return raw_lines, expected_lines

def test_redaction_accuracy():
    raw_lines, expected_lines = generate_test_data()

    # 1. Inject the known test set into the daemon's data source
    logs_path = '/app/logs.txt'
    with open(logs_path, 'w') as f:
        f.write('\n'.join(raw_lines) + '\n')

    # 2. Execute the agent's script
    script_path = '/home/user/generate_audit.py'
    assert os.path.exists(script_path), f"Agent script {script_path} not found."

    try:
        subprocess.run(['python3', script_path], check=True, timeout=30)
    except subprocess.TimeoutExpired:
        pytest.fail("Agent script timed out after 30 seconds.")
    except subprocess.CalledProcessError as e:
        pytest.fail(f"Agent script failed during execution: {e}")

    # 3. Read the output and compute the metric
    out_path = '/home/user/clean_audit.log'
    assert os.path.exists(out_path), f"Output log {out_path} was not generated by the script."

    with open(out_path, 'r') as f:
        agent_lines = f.read().splitlines()

    if len(agent_lines) != len(expected_lines):
        pytest.fail(f"Line count mismatch. Expected {len(expected_lines)} lines, but got {len(agent_lines)} lines. Accuracy is implicitly 0.0.")

    correct = 0
    for agent_line, truth_line in zip(agent_lines, expected_lines):
        if agent_line == truth_line:
            correct += 1

    accuracy = correct / len(expected_lines)

    # 4. Assert against the threshold
    assert accuracy >= 0.98, f"Redaction accuracy {accuracy:.4f} is below the required threshold of 0.98."