import os
import re

def test_corrupted_archives_log():
    log_path = "/home/user/corrupted_archives.log"
    assert os.path.isfile(log_path), f"Log file {log_path} does not exist. Did you run the Rust program?"

    toml_path = "/home/user/project_files.toml"
    assert os.path.isfile(toml_path), f"Config file {toml_path} does not exist."

    with open(toml_path, "r") as f:
        content = f.read()

    # Extract file paths from the TOML configuration
    files_match = re.search(r'files\s*=\s*\[(.*?)\]', content, re.DOTALL)
    assert files_match is not None, "Could not parse 'files' array from TOML."

    file_paths = re.findall(r'"([^"]+)"', files_match.group(1))

    # Recompute the expected corrupted archives
    expected_corrupted = []
    for path in file_paths:
        if not os.path.isfile(path):
            expected_corrupted.append(path)
            continue

        size = os.path.getsize(path)
        if size < 17:
            expected_corrupted.append(path)
            continue

        with open(path, "rb") as bf:
            bf.seek(-17, os.SEEK_END)
            signature = bf.read(17)
            if signature != b"VALID_ARCHIVE_EOF":
                expected_corrupted.append(path)

    expected_corrupted.sort()

    # Read the actual log file generated by the student's program
    with open(log_path, "r") as lf:
        log_lines = [line.strip() for line in lf if line.strip()]

    assert log_lines == expected_corrupted, (
        f"Contents of {log_path} do not match the expected corrupted archives.\n"
        f"Expected: {expected_corrupted}\n"
        f"Found: {log_lines}"
    )