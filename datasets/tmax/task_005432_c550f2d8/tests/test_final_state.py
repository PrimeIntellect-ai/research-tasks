import os
import requests
import pytest
import math

def test_cron_schedule():
    path = "/home/user/cron_schedule.txt"
    assert os.path.isfile(path), f"Cron schedule file {path} is missing."
    with open(path, "r") as f:
        content = f.read().strip()
    expected = "30 4 * * 2 /bin/bash /home/user/backup.sh"
    assert expected in content, f"Expected '{expected}' in cron schedule file, got '{content}'"

def test_api_unauthorized():
    url = "http://127.0.0.1:5000/rolling_stats"
    try:
        response = requests.get(url, timeout=5)
    except requests.exceptions.RequestException as e:
        pytest.fail(f"Failed to connect to the API at {url}: {e}")
    assert response.status_code == 401, f"Expected 401 Unauthorized for missing token, got {response.status_code}"

def test_api_authorized_and_parquet_generation():
    url = "http://127.0.0.1:5000/rolling_stats"
    headers = {"Authorization": "Bearer SECRET-99X-2024"}

    # Ensure parquet file is created/updated by the API call
    parquet_path = "/home/user/processed_data.parquet"
    if os.path.exists(parquet_path):
        os.remove(parquet_path)

    try:
        response = requests.get(url, headers=headers, timeout=10)
    except requests.exceptions.RequestException as e:
        pytest.fail(f"Failed to connect to the API at {url}: {e}")

    assert response.status_code == 200, f"Expected 200 OK, got {response.status_code}. Response: {response.text}"

    try:
        data = response.json()
    except ValueError:
        pytest.fail(f"Expected JSON response, but could not parse it. Response: {response.text}")

    assert "latest_date" in data, "Response missing 'latest_date'"
    assert "rolling_avg_3d" in data, "Response missing 'rolling_avg_3d'"

    assert data["latest_date"] == "2024-01-05", f"Expected latest_date '2024-01-05', got {data['latest_date']}"
    assert math.isclose(float(data["rolling_avg_3d"]), 40.0, rel_tol=1e-5), f"Expected rolling_avg_3d 40.0, got {data['rolling_avg_3d']}"

    assert os.path.isfile(parquet_path), f"Parquet file {parquet_path} was not generated by the API."

    # Basic check for Parquet magic bytes
    with open(parquet_path, "rb") as f:
        header = f.read(4)
        assert header == b"PAR1", f"File {parquet_path} does not appear to be a valid Parquet file (missing PAR1 magic bytes)."