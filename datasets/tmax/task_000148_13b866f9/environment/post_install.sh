import os
import zipfile
import struct

os.makedirs("/home/user/docs_incoming", exist_ok=True)

def create_elf_stub(entry_point):
    # 64-bit little-endian ELF stub
    header = bytearray(64)
    header[0:4] = b'\x7fELF'
    header[4] = 2 # 64-bit
    header[5] = 1 # little endian
    # Entry point at 0x18
    header[24:32] = struct.pack("<Q", entry_point)
    return header

# Archive 1
with zipfile.ZipFile("/home/user/docs_incoming/release_v1.zip", "w") as z:
    z.writestr("readme.md", "# Release 1\nDocs here.")
    z.writestr("firmware_main.elf", create_elf_stub(0x400080))
    z.writestr("case_part.gcode", "; Generated by Slic3r\n; TargetMachine: Prusa i3 MK3S\nG28\n")

