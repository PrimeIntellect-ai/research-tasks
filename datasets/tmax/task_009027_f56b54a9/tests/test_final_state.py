import os
import subprocess
import pytest

@pytest.fixture(scope="session", autouse=True)
def run_make_test():
    """
    Runs `make test` before executing the tests to ensure the pipeline runs,
    generates the report, and cleans up.
    """
    report_path = "/home/user/test_report.txt"
    if os.path.exists(report_path):
        os.remove(report_path)

    result = subprocess.run(
        ["make", "test"],
        cwd="/home/user",
        capture_output=True,
        text=True
    )
    assert result.returncode == 0, f"make test failed with return code {result.returncode}.\nSTDOUT:\n{result.stdout}\nSTDERR:\n{result.stderr}"

