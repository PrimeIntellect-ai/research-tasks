def test_report_content():
    """Verify the contents of test_report.txt exactly match the expected output."""
    report_path = "/home/user/test_report.txt"
    assert os.path.exists(report_path), f"{report_path} was not generated by make test."

    with open(report_path, "r") as f:
        content = f.read().strip()

    expected_lines = [
        "/success -> PASS",
        "/timeout -> PASS",
        "/error -> PASS"
    ]
    expected_content = "\n".join(expected_lines)

    assert content == expected_content, (
        f"test_report.txt content mismatch.\n"
        f"Expected:\n{expected_content}\n"
        f"Got:\n{content}"
    )

def test_api_evaluator_logic():
    """Test the compiled C interpreter directly with edge cases."""
    evaluator = "/home/user/api_evaluator"
    assert os.path.exists(evaluator), f"Compiled binary {evaluator} not found."
    assert os.access(evaluator, os.X_OK), f"Compiled binary {evaluator} is not executable."

    test_cases = [
        ("STATUS=200,TIME=150", "STATUS == 200 AND TIME < 100", "FAIL"),
        ("STATUS=404,TIME=50", "STATUS == 200 AND TIME < 100", "FAIL"),
        ("STATUS=500,TIME=20", "STATUS == 500", "PASS"),
        ("STATUS=200,TIME=250", "TIME > 200", "PASS"),
    ]

    for state, rule, expected in test_cases:
        result = subprocess.run(
            [evaluator, state, rule],
            capture_output=True,
            text=True
        )
        assert result.returncode == 0, f"api_evaluator exited with code {result.returncode} for state='{state}', rule='{rule}'"

        output = result.stdout.strip()
        assert output == expected, (
            f"Evaluator logic error for state='{state}', rule='{rule}'. "
            f"Expected '{expected}', got '{output}'"
        )

def test_mock_server_cleanup():
    """Verify that no mock_server.py processes are left running after make test."""
    result = subprocess.run(
        ["pgrep", "-f", "mock_server.py"],
        capture_output=True,
        text=True
    )
    # pgrep returns 0 if it finds a matching process, 1 if no matches are found
    assert result.returncode != 0, "mock_server.py is still running. The Makefile did not clean it up properly."