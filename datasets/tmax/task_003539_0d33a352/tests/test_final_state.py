import os
import time
import json
import subprocess
import configparser
import pytest

def test_config_token_updated():
    config_path = "/home/user/monitor_config.ini"
    assert os.path.isfile(config_path), f"Config file missing at {config_path}"

    # Read the INI file
    config = configparser.ConfigParser()
    try:
        config.read(config_path)
        # Assuming the token is under a specific section, or we can just search the text if section is unknown.
        # Let's just read the file as text to be safe if there are no sections.
        with open(config_path, 'r') as f:
            content = f.read()
            assert "824915" in content, f"The correct authorization token '824915' was not found in {config_path}"
    except Exception as e:
        pytest.fail(f"Failed to read or parse {config_path}: {e}")

def test_optimized_binary_speedup_and_correctness():
    original_bin = "/home/user/bin/monitor.bak"
    optimized_bin = "/home/user/bin/monitor"
    json_output = "/home/user/health_status.json"

    assert os.path.isfile(original_bin), f"Original binary missing at {original_bin}"
    assert os.path.isfile(optimized_bin), f"Optimized binary missing at {optimized_bin}"
    assert os.access(optimized_bin, os.X_OK), f"Optimized binary at {optimized_bin} is not executable"

    # Remove existing JSON output if any to ensure it's generated by the run
    if os.path.exists(json_output):
        os.remove(json_output)

    def measure_time(binary_path):
        start = time.time()
        result = subprocess.run([binary_path, "--run-once"], stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        duration = time.time() - start
        assert result.returncode == 0, f"Binary {binary_path} failed with return code {result.returncode}"
        return duration

    # Measure optimized binary
    new_time = measure_time(optimized_bin)

    # Verify JSON was created and is valid
    assert os.path.isfile(json_output), f"JSON output file {json_output} was not created by the optimized binary"
    try:
        with open(json_output, 'r') as f:
            data = json.load(f)
            assert isinstance(data, dict), "JSON output should be a dictionary"
    except json.JSONDecodeError:
        pytest.fail(f"Output file {json_output} does not contain valid JSON")

    # Measure original binary
    original_time = measure_time(original_bin)

    # Calculate speedup
    speedup = original_time / new_time if new_time > 0 else float('inf')

    assert speedup >= 5.0, f"Execution speedup is {speedup:.2f}x, which is below the threshold of 5.0x (Original: {original_time:.4f}s, Optimized: {new_time:.4f}s)"

def test_systemd_service_active():
    # Verify that the systemd service is active (or at least enabled/configured to run)
    # Since it's a user service, we use systemctl --user
    try:
        result = subprocess.run(
            ["systemctl", "--user", "is-active", "health-monitor.service"],
            capture_output=True,
            text=True
        )
        # We don't strictly fail if it's not active, but the prompt says "ensure the service can start successfully".
        # If it's active, great. If not, maybe they didn't start it but fixed it.
        # Let's just check if it's not failed.
        result_failed = subprocess.run(
            ["systemctl", "--user", "is-failed", "health-monitor.service"],
            capture_output=True,
            text=True
        )
        assert result_failed.stdout.strip() != "failed", "The health-monitor.service is in a failed state."
    except Exception as e:
        # Ignore if systemctl is not fully available in the test environment
        pass