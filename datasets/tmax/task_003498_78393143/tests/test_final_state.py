import os
import pytest
import pandas as pd

def test_services_running():
    """Check if Nginx, Flask, Redis, and the C worker are running."""
    # We can check if ports are listening
    import socket

    def is_port_open(port):
        with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
            return s.connect_ex(('localhost', port)) == 0

    assert is_port_open(8080), "Nginx is not listening on port 8080"
    assert is_port_open(5000), "Flask API is not listening on port 5000"
    assert is_port_open(6379), "Redis is not listening on port 6379"

def test_worker_output_accuracy():
    """
    Evaluate the accuracy of the cleaned output generated by the C worker.
    The output should match the golden dataset with an accuracy >= 0.95.
    """
    agent_output_path = '/tmp/cleaned_output.csv'
    golden_output_path = '/app/golden_output.csv'

    assert os.path.exists(agent_output_path), f"Output file {agent_output_path} not found. Did the C worker process the data?"
    assert os.path.exists(golden_output_path), f"Golden output file {golden_output_path} not found."

    try:
        agent_out = pd.read_csv(agent_output_path, header=None)
    except Exception as e:
        pytest.fail(f"Failed to read {agent_output_path}: {e}")

    try:
        golden_out = pd.read_csv(golden_output_path, header=None)
    except Exception as e:
        pytest.fail(f"Failed to read {golden_output_path}: {e}")

    # Join on record_id (column 0)
    merged = pd.merge(golden_out, agent_out, on=0, how='left', suffixes=('_gold', '_agent'))

    correct = 0
    for _, row in merged.iterrows():
        # If record is missing in agent output
        if pd.isna(row['1_agent']):
            continue

        # Check ref_id (column 6)
        if row['6_gold'] != row['6_agent']:
            continue

        # Check floats (columns 1 to 5)
        try:
            diffs = [abs(float(row[f'{i}_gold']) - float(row[f'{i}_agent'])) for i in range(1, 6)]
            if all(d <= 0.05 for d in diffs):
                correct += 1
        except ValueError:
            # If conversion to float fails, it's incorrect
            continue

    accuracy = correct / len(golden_out)
    assert accuracy >= 0.95, f"Accuracy metric failed: {accuracy:.4f} < 0.95 threshold. The C worker output does not match the golden dataset closely enough."