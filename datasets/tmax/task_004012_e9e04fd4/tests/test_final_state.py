import os
import sys
import json
import time
import math
import pytest

def test_benchmark_results_json():
    """
    Reads the benchmark_results.json file generated by the agent and verifies
    that the keys exist and meet the required thresholds.
    """
    json_path = '/home/user/benchmark_results.json'
    assert os.path.exists(json_path), f"JSON file missing at {json_path}"

    with open(json_path, 'r') as f:
        try:
            data = json.load(f)
        except json.JSONDecodeError:
            pytest.fail(f"File {json_path} is not valid JSON.")

    assert 'mse' in data, "Key 'mse' missing in JSON"
    assert 'speedup' in data, "Key 'speedup' missing in JSON"

    mse = float(data['mse'])
    speedup = float(data['speedup'])

    assert mse < 1e-5, f"Reported MSE too high: {mse} (threshold: < 1e-5)"
    assert speedup >= 3.0, f"Reported Speedup too low: {speedup}x (threshold: >= 3.0x)"

def test_derived_metrics():
    """
    Re-derives the metrics by importing both the pure Python and Rust engines,
    running the trajectory computation with the extracted parameters, and
    calculating the MSE and speedup directly.
    """
    project_dir = '/home/user/math_ws_project'
    sys.path.insert(0, project_dir)

    try:
        import pure_python
    except ImportError:
        pytest.fail("Could not import pure_python.py. Ensure it exists in the project directory.")

    try:
        import rust_engine
    except ImportError:
        pytest.fail("Could not import rust_engine. Ensure the Rust extension was built and installed correctly.")

    # Parameters extracted from /app/sys_config.png
    alpha = 1.75
    beta = 0.95
    iterations = 1000000

    # Run pure Python implementation
    t0 = time.time()
    py_res = pure_python.compute_trajectory(alpha, beta, iterations)
    t1 = time.time()
    py_time = t1 - t0

    # Run Rust implementation
    t0 = time.time()
    rs_res = rust_engine.compute_trajectory(alpha, beta, iterations)
    t1 = time.time()
    rs_time = t1 - t0

    assert len(py_res) == iterations, f"Pure Python returned {len(py_res)} iterations, expected {iterations}"
    assert len(rs_res) == iterations, f"Rust engine returned {len(rs_res)} iterations, expected {iterations}"

    # Calculate Mean Squared Error
    mse = sum((px - rx)**2 + (py - ry)**2 for (px, py), (rx, ry) in zip(py_res, rs_res)) / iterations

    # Calculate Speedup
    speedup = py_time / rs_time if rs_time > 0 else float('inf')

    # Assert metrics
    assert mse < 1e-5, f"Derived MSE too high: {mse} (threshold: < 1e-5)"
    assert speedup >= 3.0, f"Derived Speedup too low: {speedup:.2f}x (threshold: >= 3.0x)"